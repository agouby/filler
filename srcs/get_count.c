#include "filler.h"

void	count_pos(t_fill *fill, char **line)
{
	size_t i;

	i = 0;
	while ((*line)[i])
	{
		if ((*line)[i] == 'X' || (*line)[i] == 'x')
			fill->xc++;
		else if ((*line)[i] == 'O' || (*line)[i] == 'o')
			fill->oc++;
		i++;
	}
}
